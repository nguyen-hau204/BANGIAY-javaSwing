    boolean check(){     
        if(txtMaspct.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Mã sản phẩm chi tiết không thể để trống");
            txtMaspct.requestFocus();
            return false;
        }else if(txtGia.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Gía không được để trống");
           txtGia.requestFocus();
            return false;
            
        }else if(txtGia.getText().matches("\\s+")){
            JOptionPane.showMessageDialog(this, "Gía không được để khoảng trống space");
            txtGia.setText("");
            txtGia.requestFocus();
            return false;
        }else if(txtSoLuong.getText().matches("\\s+")){
            JOptionPane.showMessageDialog(this, "Số lượng không được để khoảng trống space");
            txtSoLuong.setText("");
            txtSoLuong.requestFocus();
            return false;
        }else if(Integer.parseInt(txtSoLuong.getText())<0 || Integer.parseInt(txtSoLuong.getText()) == 0 ){
            JOptionPane.showMessageDialog(this, "Số lượng không được < 0 hoặc = 0 ");
            txtSoLuong.setText("");
            txtSoLuong.requestFocus();
            return false; 
        }else if(Double.parseDouble(txtGia.getText())<0 || Double.parseDouble(txtGia.getText()) == 0 ){
            JOptionPane.showMessageDialog(this, "Gía không được < 0 hoặc = 0 ");
            txtGia.setText("");
            txtGia.requestFocus();
            return false; 
        }else if(txtSoLuong.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống ");
            txtSoLuong.requestFocus();
            return false;
        }else{
       
            return true;
        }
    }
